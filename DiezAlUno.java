public class DiezAlUno {

public static void main(String[] args) {
int numeros = 10; //La variable numeros es igual a 10 que es desde lo que tenemos que iniciar
	do { //Mientras el numero sea mayor o igual que 1 y menos o igual que 10 se resta 1 en cada iteracion del bucle do while
		System.out.print(numeros + " "); //Se imprime el 10 antes de restar el primer numero, sino empezaria desde el 9 porque se restaria antes de hacer el print
		numeros--;
	
	
	}while((numeros>=1) && (numeros<=10));
	
}
}	





